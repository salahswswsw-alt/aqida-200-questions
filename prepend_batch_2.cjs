function parseExactly(text) {
    let clean = text.trim();
    if (clean.startsWith('[') && clean.endsWith(']')) {
        clean = clean.substring(1, clean.length - 1).trim();
    }

    const items = [];
    const questionSplits = clean.split(/س (\d+) :/);
    
    for (let i = 1; i < questionSplits.length; i += 2) {
        const id = parseInt(questionSplits[i]);
        const content = questionSplits[i+1];
        
        const junc = content.indexOf('ج :');
        if (junc === -1) continue;
        
        const qRaw = content.substring(0, junc);
        const aRaw = content.substring(junc + 3);
        
        let aClean = aRaw.split('________________________________________')[0].trim();
        let qClean = qRaw.trim();
        
        let category = "الإيمان بالرسل";
        if (id >= 102) category = "الإيمان باليوم الآخر";

        items.push({
            id: id,
            question: qClean,
            answer: aClean,
            category: category
        });
    }
    return items;
}

