// Pre-process raw text into cleaner chunks by splitting at question starts
// but keeping exact characters.
function parseExactly(text) {
    // Strip the outer [ and ] if they exist (user provided them in the prompt)
    let clean = text.trim();
    if (clean.startsWith('[') && clean.endsWith(']')) {
        clean = clean.substring(1, clean.length - 1).trim();
    }

    const items = [];
    const questionSplits = clean.split(/س (\d+) :/);
    
    // index 0 is irrelevant or preamble
    for (let i = 1; i < questionSplits.length; i += 2) {
        const id = parseInt(questionSplits[i]);
        const content = questionSplits[i+1];
        
        // Find junction of Q and A: "ج :"
        const junc = content.indexOf('ج :');
        if (junc === -1) continue;
        
        const qRaw = content.substring(0, junc);
        const aRaw = content.substring(junc + 3);
        
        // Final cleaning of answer up to the next question or end
        // But since we split by regex, aRaw is already delimited.
        // We just need to remove the separator line if present.
        let aClean = aRaw.split('________________________________________')[0].trim();
        let qClean = qRaw.trim();
        
        let category = "الملائكة";
        if (id >= 77 && id <= 86) category = "الإيمان بالكتب";
        if (id >= 87) category = "الإيمان بالرسل";

        items.push({
            id: id,
            question: qClean,
            answer: aClean,
            category: category
        });
    }
    return items;
}

